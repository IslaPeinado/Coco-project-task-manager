    ADD CONSTRAINT cocouser_email_key UNIQUE (email);

ALTER TABLE cocouser
    ADD CONSTRAINT cocouser_login_key UNIQUE (login);

ALTER TABLE project
    ADD CONSTRAINT project_name_key UNIQUE (name);

ALTER TABLE role
    ADD CONSTRAINT role_role_name_key UNIQUE (role_name);

CREATE INDEX idx_task_status_sort_order ON task_status (sort_order);

ALTER TABLE tasks
    ADD CONSTRAINT fk_tasks_assignee FOREIGN KEY (assigned_to) REFERENCES cocouser (id) ON DELETE SET NULL;

CREATE INDEX idx_tasks_assigned_to ON tasks (assigned_to);

ALTER TABLE tasks
    ADD CONSTRAINT fk_tasks_project FOREIGN KEY (project_id) REFERENCES project (id) ON DELETE CASCADE;

CREATE INDEX idx_tasks_project_id ON tasks (project_id);

ALTER TABLE tasks
    ADD CONSTRAINT fk_tasks_status FOREIGN KEY (status) REFERENCES task_status (status) ON DELETE RESTRICT;

CREATE INDEX idx_tasks_status ON tasks (status);

ALTER TABLE cocouser_project_role
    ADD CONSTRAINT fk_upr_project FOREIGN KEY (project_id) REFERENCES project (id) ON DELETE CASCADE;

CREATE INDEX idx_upr_project_id ON cocouser_project_role (project_id);

ALTER TABLE cocouser_project_role
    ADD CONSTRAINT fk_upr_role FOREIGN KEY (role_id) REFERENCES role (id) ON DELETE CASCADE;

CREATE INDEX idx_upr_role_id ON cocouser_project_role (role_id);

ALTER TABLE cocouser_project_role
    ADD CONSTRAINT fk_upr_user FOREIGN KEY (user_id) REFERENCES cocouser (id) ON DELETE CASCADE;

CREATE INDEX idx_upr_user_id ON cocouser_project_role (user_id);